import Foundation

public class MathStuff {
    
    func addNumbers(x: Int, y: Int) -> Int {
        return x + y
    }
    
    func multipleNumbers(x: Int, y: Int) -> Int {
        return x * y
    }
    
    func divideNumbers(x: Int, y: Int) -> Int {
        return x / y
    }
    
}
